def dump(**kwargs):
    return kwargs

ret = dump(**{'x':1},y=2,**{'z':3,'m':4})
print(ret)  # {'x': 1, 'y': 2, 'z': 3,'m': 4}

# 在要传递字典到函数参数的时候，**操作符用于字典拆包

d1={'a':1,'b':2}
d2={'a':3,'b':5,'c':6,'d':7}
ret = d1|d2
# 字典合并，key相同的，后来的值会把前面的值覆盖
# 字典合并后，是创建了一个新的字典
print(id(ret),ret) # {'a': 3, 'b': 5, 'c': 6, 'd': 7}
print(id(d1),d1)
print(id(d2),d2)

# 如果使用|=操作符，相当于把两个字典中的所有元素的集合都放在第一个字典中
# |=操作合并字典的结果是，没有创建新的字典
d1 |= d2
print(d1)
print(d2)
